package uk.ac.aber.cs211.group07.system.resources;


/**
 * MapObject interface characterises every class that represents an object on the board.
 * @author Julia Wojciechowska
 * @version 0.1 - 06/03/17 - initial creation /juw30
 * @version 1.0 - 05/05/17 - Ready for initial release /haa14
 */
public interface MapObject {

	/**
    * Sets a position of an object on the board
	* @param pos position of that object
	*/
   public void setPos(Position pos);
   
   /**
    * Sets a position of an object on the board
    * @param x coordinate
    * @param y coordinate
    */
   public void setPos(int x, int y);
   
   /**
    * Gets a position of an object on the board
    * @return position of an object
    */
   public Position getPos();
   
   /**
    * Gets a size of an object on the board
    * @return size of an object
    */
   public Position getSize();
   
   /**
    * Sets a size of an object on the board
    * @param size of an object
    */
   public void setSize(Position size);
   
   /**
    * Sets a size of an object on the board
    * @param x horizontal
    * @param y vertical
    */
   public void setSize(int x, int y);

}