package uk.ac.aber.cs211.group07.system.exceptions;

/**
 * Thrown when an invalid move is to be made (e.g. moving over/on an Island)
 * @author Kamil Cupial
 * @version 0.1 - 19/03/17 - initial creation /kac12
 * @version 1.0 - 05/05/17 - Ready for initial release /haa14
 */
@SuppressWarnings("serial")
public class InvalidMoveException extends Exception {
	/**
	 * The exception default constructor
	 */
	public InvalidMoveException(){
		super("Invalid move for current position and direction.");
	}
}
